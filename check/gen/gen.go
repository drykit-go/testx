package gen

import (
	"fmt"
	"html/template"
	"os"
	"os/exec"
	"strings"
	"time"

	"github.com/drykit-go/testix/strcase"
)

var tplFuncs = template.FuncMap{
	"camelcase": strcase.Camel,
}

func File(tpl, out string) error {
	t, err := template.
		New(tpl).
		Funcs(tplFuncs).
		ParseFiles(tpl)
	if err != nil {
		return err
	}

	f, err := createGenFile(out)
	if err != nil {
		return err
	}

	if err := t.Execute(f, types); err != nil {
		return err
	}

	return runFormatter(out)
}

func createGenFile(filepath string) (*os.File, error) {
	f, err := os.Create(filepath)
	if err != nil {
		return nil, err
	}
	if _, err := f.WriteString(generateHeader()); err != nil {
		return nil, err
	}
	return f, nil
}

func generateHeader() string {
	s := strings.Builder{}
	s.WriteString("// Code generated by go generate ./...; DO NOT EDIT\n")
	s.WriteString(fmt.Sprintf(
		"// Last generated on %s\n\n",
		time.Now().UTC().Format(time.RFC822),
	))
	return s.String()
}

func runFormatter(filepath string) error {
	cmd := exec.Command("goimports", "-w", filepath)
	return cmd.Run()
}
